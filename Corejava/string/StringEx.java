public class StringEx {

	public static void main(String[] args) {

		String name = "Hyderabad";

		name.concat(" Biryani");

		System.out.println(name);
		
		

		StringBuffer name1 = new StringBuffer("Hyderabad");

		name1.append(" Biryani");

		System.out.println(name1);

	}

}
